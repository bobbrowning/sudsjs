let suds = require('../../../config/suds');

let lang = require('../../../config/language')['EN'];
let displayField = require('../display-field');


let fn = async function (fieldType, fieldName, fieldValue, attributes, errorMsg) {
  trace = require('track-n-trace');
  trace.log(arguments);

  let results = '';
  let display = await displayField(attributes, fieldValue);
  results = ` 
          <input type="hidden" 
            name="${fieldName}"   
            id="${fieldName}" 
            value="${fieldValue}"
          >
             ${display}
           <span id="err_${fieldName}" class="sudserror"> ${errorMsg}</span>
  `;
  return (results);


}

exports.documentation = documentation;
exports.fn = fn;
